package io.gs2.guard.request;

import java.util.*;
import java.util.stream.Collectors;
import java.util.stream.StreamSupport;
import java.io.Serializable;
import com.fasterxml.jackson.annotation.JsonIgnoreProperties;
import com.fasterxml.jackson.databind.ObjectMapper;
import com.fasterxml.jackson.databind.JsonNode;
import io.gs2.core.control.Gs2BasicRequest;
import io.gs2.guard.model.BlockingPolicyModel;

@SuppressWarnings("serial")
@JsonIgnoreProperties(ignoreUnknown=true)
public class CreateNamespaceRequest extends Gs2BasicRequest<CreateNamespaceRequest> {
    private String name;
    private String description;
    private BlockingPolicyModel blockingPolicy;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public CreateNamespaceRequest withName(String name) {
		this.name = name;
		return this;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public CreateNamespaceRequest withDescription(String description) {
		this.description = description;
		return this;
	}
	public BlockingPolicyModel getBlockingPolicy() {
		return blockingPolicy;
	}
	public void setBlockingPolicy(BlockingPolicyModel blockingPolicy) {
		this.blockingPolicy = blockingPolicy;
	}
	public CreateNamespaceRequest withBlockingPolicy(BlockingPolicyModel blockingPolicy) {
		this.blockingPolicy = blockingPolicy;
		return this;
	}

    public static CreateNamespaceRequest fromJson(JsonNode data) {
        if (data == null) {
            return null;
        }
        return new CreateNamespaceRequest()
            .withName(data.get("name") == null || data.get("name").isNull() ? null : data.get("name").asText())
            .withDescription(data.get("description") == null || data.get("description").isNull() ? null : data.get("description").asText())
            .withBlockingPolicy(data.get("blockingPolicy") == null || data.get("blockingPolicy").isNull() ? null : BlockingPolicyModel.fromJson(data.get("blockingPolicy")));
    }

    public JsonNode toJson() {
        return new ObjectMapper().valueToTree(
            new HashMap<String, Object>() {{
                put("name", getName());
                put("description", getDescription());
                put("blockingPolicy", getBlockingPolicy() != null ? getBlockingPolicy().toJson() : null);
            }}
        );
    }
}